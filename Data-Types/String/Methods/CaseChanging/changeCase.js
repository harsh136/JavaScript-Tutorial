// to chnage the case of a string either from upper to lower or lower to upper we use two methode
let someString = `interface`; //this in lowercase
console.log(someString.toUpperCase()); // now it is changed to uppercase: INTERFACE

let someOtherString = `THIS IS ANOTHER EXAMPLE`;
console.log(someOtherString.toLowerCase()); // this will change the case to lowercase
