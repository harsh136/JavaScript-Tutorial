// Basic Arithmetic:
// +  Addition
// -  Subtraction
// *  Multiplication
// /  Division
// %  Modulus (remainder)

let a = 10,
  b = 3;
console.log(a + b); // 13
console.log(a - b); // 7
console.log(a * b); // 30
console.log(a / b); // 3.333333...
console.log(a % b); // 1 (remainder)

// Special behaviors:
// String concatenation with +
console.log("Hello" + " " + "World"); // "Hello World"
console.log("5" + 3); // "53" (string concatenation)
console.log("5" - 3); // 2 (numeric subtraction)

// Unary plus/minus
console.log(+"5"); // 5 (converts to number)
console.log(-"5"); // -5
